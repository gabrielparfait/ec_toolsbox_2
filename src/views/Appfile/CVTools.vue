    <h4>Point de vigilance : l’impact de l’usage des logiciels ATS sur le traitement de votre CV</h4>
    <p>
      Les entreprises utilisent de plus en plus des ATS (Applicant Tracking Systems), 
      des logiciels de gestion des candidatures qui centralisent les CV reçus, analysent 
      leur contenu et réalisent un premier tri automatisé. Ces systèmes extraient le texte 
      de votre CV, recherchent des mots‑clés liés au poste, vérifient la présence de sections 
      standard, comparent les compétences avec le profil recherché et classent les candidatures 
      en conséquence. Un CV mal structuré ou trop graphique peut donc être mal interprété ou 
      automatiquement rejeté.
      Plus votre CV est graphique (colonnes, icônes, tableaux, images, jauges, etc.), 
      plus il risque d’être illisible pour un ATS. Ces logiciels lisent le texte brut, de haut 
      en bas et de gauche à droite, et ignorent les éléments décoratifs. Des tableaux ou des colonnes 
      peuvent perturber totalement l’ordre de lecture, rendant une partie du contenu invisible.
      Pour améliorer la compatibilité de votre CV avec les ATS, il est préférable :
    </p>
    <p><ul>
      <li>
        d’utiliser des intitulés de sections standards facilement reconnus par les systèmes, comme « Expérience professionnelle », « Formation »,<br>« Compétences », etc.
      </li>
      <li>
        de privilégier les formats .docx ou PDF.
      </li>
      <li>
        de choisir une mise en page sur une seule colonne avec des polices classiques (Arial, Calibri, Times New Roman, Helvetica). 
      </li>
      <li>
        d’éviter les éléments que les ATS lisent mal : jauges, icônes, logos d’entreprises, ou encore en‑têtes et pieds de page, souvent ignorés dans leur intégralité. 
      </li>
      <li>
        d’intégrer des mots‑clés pertinents issus de l’offre d’emploi puisque les ATS comparent les compétences, titres de postes et outils mentionnés dans votre CV avec ceux demandés dans l’annonce. Certes, un CV standardisé ressort plus facilement à travers les filtres ATS, mais il doit ensuite rester suffisamment personnalisé et attractif visuellement pour retenir l’attention d’un recruteur humain.
      </li>
      
    </ul>
